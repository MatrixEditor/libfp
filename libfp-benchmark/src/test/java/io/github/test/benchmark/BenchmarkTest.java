package io.github.test.benchmark; //@date 14.11.2023

import io.github.libfp.benchmark.*;
import io.github.libfp.profile.IProfileContext;
import io.github.libfp.threshold.IThresholdConfig;
import org.jetbrains.annotations.NotNull;

import java.io.File;
import java.io.IOException;

/**
 * The abstract class for conducting benchmark tests.
 *
 * @param <C> The type parameter representing the context for the benchmark.
 */
public abstract class BenchmarkTest<C>
{

    private final IProfileContext<C> context;
    private final DataSet dataSet;
    private final TestSuite<C> testSuite;

    /**
     * Constructor for the BenchmarkTest class.
     *
     * @param factory The factory for creating the test suite.
     */
    public BenchmarkTest(ITestSuiteFactory<C, TestSuite<C>> factory)
    {
        context = createContext();
        dataSet = createDataSet();
        testSuite = new TestSuiteBuilder<>(factory)
                .setContext(context)
                .setDataSet(dataSet)
                .createTestSuite();
    }

    @NotNull
    public static DataSet getDataSet(String targetDir, String extension)
    {
        // Specify the root directory of your dataset, which takes the form
        // as described in {@link DataSet}.
        String baseDir = "../demo";

        // The Android framework JAR storing all subbed classes (we only need
        // the class structure).
        String frameworkPath = "../android.jar";

        // The path to a file storing the ground truth app mapping. For more
        // details on the specific file format, please refer to the
        // GroundTruth class.
        String groundTruthPath = "../demo/100apps-ground-truth.txt";

        // 1. Create the ground truth and the dataset to use
        GroundTruth groundTruth = null;
        try {
            groundTruth = new GroundTruth(groundTruthPath);
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
        return new DataSet(
                baseDir,
                // Define the subdirectory, which will contain all profiles
                // generated by the TestSuite.
                targetDir,
                frameworkPath,
                // Each profile type should be identified by its extension.
                // It is recommended to end an extension with ".fp" to mark the
                // file as a profile.
                extension,
                groundTruth
        );
    }

    /**
     * Create the dataset for the benchmark. This method should be implemented
     * by subclasses.
     *
     * @return The created dataset.
     */
    protected abstract DataSet createDataSet();

    /**
     * Create the context for the benchmark. This method should be implemented
     * by subclasses.
     *
     * @return The created context.
     */
    protected abstract IProfileContext<C> createContext();

    /**
     * Get the context for the benchmark.
     *
     * @return The context.
     */
    public IProfileContext<C> getContext()
    {
        return context;
    }

    /**
     * Get the dataset for the benchmark.
     *
     * @return The dataset.
     */
    public DataSet getDataSet()
    {
        return dataSet;
    }

    /**
     * Get the test suite for the benchmark.
     *
     * @return The test suite.
     */
    public TestSuite<C> getTestSuite()
    {
        return testSuite;
    }

    /**
     * Prepare the test suite for the given app name.
     *
     * @param appName The name of the app.
     */
    public void prepareTestSuite(final String appName)
    {
        testSuite.prepareApp(appName);
        testSuite.prepareLibraries();
    }


    /**
     * Perform the benchmark test for the specified app and configuration.
     *
     * @param appName The name of the app.
     * @param config  The threshold configuration for the benchmark.
     */
    public void benchmarkTest(
            final String appName,
            final IThresholdConfig config)
    {
        // 1. Prepare all profiles in the test suite.
        prepareTestSuite(appName);

        // 2. perform the benchmark using the TestSuite<C>
        BenchmarkResult result =
                testSuite.benchmark(appName, config);

        inspectBenchmarkResult(appName, config, result);
    }

    protected void inspectBenchmarkResult(
            String appName,
            IThresholdConfig config,
            BenchmarkResult result)
    {
        // 3. retrieve the version-level and library-level whitelists
        Whitelist versionWhitelist = dataSet
                .groundTruth()
                .getVersionWhitelist(appName);

        Whitelist libWhitelist = dataSet
                .groundTruth()
                .getLibraryWhitelist(appName);

        double threshold = config.getThreshold(TestResult.class);
        System.out.printf("Test Results (%s):\n", appName);
        for (final String appType : result.getTestedAppTypes()) {
            // 4. for each tested app type calculate the test accuracy
            TestAccuracy versionAccuracy = result.getTestAccuracy(
                    appType, threshold, versionWhitelist);

            TestAccuracy libAccuracy = result.getTestAccuracy(
                    appType, threshold, libWhitelist);

            System.out.println("-".repeat(50));
            if (appType.equals(BenchmarkResult.defaultAppType)) {
                System.out.println("[default]:");
            } else {
                System.out.printf("[%s]:\n", appType);
            }

            System.out.printf("  - lib    : %s\n", libAccuracy);
            System.out.printf("  - version: %s\n", versionAccuracy);
        }
    }

    public void testOne(
            final String appName, final String appType,
            final IThresholdConfig config)
    {
        File file = new File(dataSet.getApplicationFilePath(appName, appType));
        BenchmarkResult result = testSuite
                .benchmark(appName, config, true, new File[]{file});

        inspectBenchmarkResult(appName, config, result);
    }
}
